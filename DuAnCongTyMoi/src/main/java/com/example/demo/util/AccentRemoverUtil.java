    /**
     * Removes the accent.
     *
     * @param ch
     *            the ch
     * @return the char
     */
    public static char removeAccent(char ch) {
        int index = Arrays.binarySearch(SPECIAL_CHARACTERS, ch);

        if (index >= 0) {
            ch = REPLACEMENTS[index];
        }

        return ch;
    }

    /**
     * Removes the accent.
     *
     * @param textString
     *            the text string
     * @return the string
     */
    public static String removeAccent(String textString) {
        if (textString == null) {
            return StringPool.BLANK;
        }

        StringBuilder sb = new StringBuilder();

        for (int i = 0; i < textString.length(); i++) {
            sb.append(removeAccent(textString.charAt(i)));
        }

        return sb.toString();
    }

    /**
     * Removes the non vietnamese accent.
     *
     * @param textString
     *            the text string
     * @return the string
     */
    public static String removeNonVietnameseAccent(String textString) {
        if (textString == null) {
            return StringPool.BLANK;
        }

        StringBuilder sb = new StringBuilder();

        for (int i = 0; i < textString.length(); i++) {
            char ch = textString.charAt(i);

            int index = Arrays.binarySearch(VIETNAMESE_CHARACTERS, ch);

            if (index < 0) {
                ch = CharPool.AMPERSAND;
            }

            sb.append(ch);
        }

        return sb.toString().replaceAll(StringPool.AMPERSAND, StringPool.BLANK);
    }
